-- Looking at the total cases vs the population

select Location,date, total_cases, population, (total_deaths/population)*100 as population_infected
from project_sql.covid_deaths
where Location like '%India'
order by 1,2;

-- comparing the infection rate of the countries 

select Location,population, MAX(total_cases) as HighestInfectionRate,  Max((total_cases/population))*100 as infection_rate_country
